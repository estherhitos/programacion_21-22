public class Ej4 {
	
	public static void main (String[] args) {
		
		System.out.println("Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás");
		
		for(int i = 320; i >= 160; i-=20) {
			System.out.println(i);
			} 
		
	}
}

